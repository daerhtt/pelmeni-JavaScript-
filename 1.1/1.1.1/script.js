// Исходное число для проверки 
let number = -5; 

// Проверяем, является ли число отрицательным (меньше нуля) 
if (number < 0) { 
    // Если число меньше нуля, выводим сообщение об отрицательном числе 
    console.log("Число отрицательное"); 
} else { 
    // Если число больше или равно нулю, выводим сообщение о неотрицательном числе 
    console.log("Число неотрицательное"); 
}